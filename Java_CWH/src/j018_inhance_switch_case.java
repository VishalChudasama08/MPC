public class j018_inhance_switch_case {
    public static void main(String[] args) {
        String a;
        a = "Vishal";
        switch (a) {
            case "Vishal" -> {
                System.out.println("You are Adult");
                System.out.println("You are everything");
                System.out.println("You are mend bowing");
            }
            case "nthing1" -> System.out.println("You are nthing1");
            case "nthing2" -> System.out.println("You are nthing2");
            case "nthing3" -> System.out.println("You are nthing3");
            default -> System.out.println("This is default");
        }
    }
}

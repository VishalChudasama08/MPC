// Mathematic Operators
let a=10;
let b=2;
console.log("a = 10");
console.log("b = 2");
console.log("a + b =",(a+b));
console.log("a - b =",(a-b));
console.log("a * b =",(a*b));
console.log("a / b =",(a/b));
console.log("a % b =",(a%b));
console.log("a ** b =",(a**b));
// ** is like 10²

console.log("\na++ =",(a++));
// First a print then ++
console.log("a =",(a));
console.log("++a =",(++a));
// First ++ then print a
console.log("a =",(a));
console.log("a-- =",(a--));
console.log("a =",(a));
console.log("--a =",(--a));
console.log("a =",(a));

let c=8;
console.log("\nc = 8");
console.log("c += 2 ->c =",(c+=2));
console.log("c -= 2 ->c =",(c-=2));
console.log("c *= 2 ->c =",(c*=2));
console.log("c /= 2 ->c =",(c/=2));
console.log("c %= 2 ->c =",(c%=2));
console.log("c += 8 ->c =",(c+=8));
console.log("c **= 2 ->c =",(c**=2));
// **= is like 8²

